package tema_cts.flyweight;

public class Pharmacy {
	private String name;
	private String address;
	
	public Pharmacy(String name, String address) {
		super();
		this.name = name;
		this.address = address;
	}

	public String getName() {
		return name;
	}

	public String getAddress() {
		return address;
	}
	
	
}
